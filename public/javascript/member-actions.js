/* JavaScript for member pages. */


function showStudentList()
{
	var listDiv = document.getElementById("studentListDiv");
	
	if( listDiv.style.display === "none" )
	{
		listDiv.style.display = "block";
	}
	else
	{
		listDiv.style.display = "none";
	}
}


