<section class="project-summary">
	
	<h1 class="project-summary">An interactive 3D Tutorial Lab experience for Students of Physics and Engineering.</h1>
	
	<h2>Introduction</h2>
	
	<p>
	Many Students drop out of classes in Physics and Engineering due to the high level of difficulty in understanding these topics. Often, attending lectures and reading textbooks is just not enough. A different approach is needed to make the material easier to understand. Physics in Motion uses today's powerful interactive online 3D gaming technologies to create a new type of online science tutorial. Not for a game, but for the creation of visual, immersive, and hands on learning experiences in the form of tutorial labs that let the Student explore. The goal of Physics in Motion is a more intuitive understanding based on the firsthand experience of a subject.
	</p>

	<h2>Tutorial Labs</h2>
		
	<p>
	Three or four proof of concept tutorial labs are being developed for Physics in Motion. They are Linear Motion and Collisions, Beginning RC Circuits, Vector Calculus of 3D Volumes and Surfaces, and the Photoelectric Effect from Quantum Mechanics. These tutorial labs will take full advantage of the power of 3D gaming technology available today. Each tutorial lab will be animated and fully interactive to make the topics more approachable, interesting, and informative. Student perform hands-on interactive experiments and activities directly related to the concepts they are trying to learn. Measurements in the tutorial labs will be collected electronically, as if in a standard industrial lab. The Student will then analyze the collected data with interactive graphs and math calculations done inside the tutorial lab. At the end of a tutorial lab, a summary will be provided including data, graphs, equations, and problems encountered.
	</p>

	<h2>Automated Mentor</h2>
	
	<p>
	Along the way, the Automated Mentor will explain topics, give hints, and provide corrections as needed. If the Student asks for help during the tutorial lab, the Automated Mentor will highlight problem areas and offer hints. For math calculations, the Automated Mentor looks for errors as the Student solves the equations. These errors, problems, and hints are later documented as reminders in the lab summary. If enough Students encounter the same problem, error, or hint in a tutorial lab, it will be reported to the Professor as a topic that may need more help in the classroom.
	</p>

	<h2>Web Services</h2>
	
	<p>
	 Loging in provides access to the Student and Professor services. The Student pages include lists of assigned and completed tutorial labs, links to send a notice of tutorial lab completion to a Professor, and links to download lab summaries. The Professor pages include lists of assigned tutorial labs, which labs each Student has completed or incomplete, and a form to assign labs to Students. The Professor also have links to download lab summaries, and lists of frequent problems encountered by Students for a particular tutorial lab. The tutorial lab information pages have a descriptions of the tutorial labs, topics to be covered, prior knowledge required, and tutorial lab ratings. 
	</p>
	
